CHAR_WIDTH = 3
CHAR_HEIGHT = 5
CHAR_SPACING = 1


def get_char(char: str) -> list[list[int]]:
    """Get pixel data for a character."""
    return FONT_3X5.get(char.upper(), FONT_3X5[" "])


def calculate_text_width(text: str) -> int:
    """Calculate total width in modules for text."""
    if not text:
        return 0
    return len(text) * CHAR_WIDTH + (len(text) - 1) * CHAR_SPACING


def calculate_text_height() -> int:
    """Calculate height in modules for text."""
    return CHAR_HEIGHT
